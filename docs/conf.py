# ---------------------------------------------------------------------------
# Logging filter — suppress duplicate-object description warnings
# ---------------------------------------------------------------------------
# "duplicate object description" is emitted when re-exported symbols in
# __init__.py are documented twice.  Sphinx 8.2 emits this without a type tag
# so suppress_warnings cannot catch it; a logging filter is required.
# ---------------------------------------------------------------------------
class _SuppressDuplicateObject(logging.Filter):
    def filter(self, record: logging.LogRecord) -> bool:
        return "duplicate object description" not in record.getMessage()


logging.getLogger("sphinx").addFilter(_SuppressDuplicateObject())

