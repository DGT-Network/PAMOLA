# ---------------------------------------------------------------------------
# Auto-discovery: public submodules → docs/index.rst
# ---------------------------------------------------------------------------
# Parses pamola_core/__init__.py with ast to find every top-level
# ``from pamola_core.X import ...`` statement, then writes docs/index.rst
# with a complete autosummary directive listing all public modules.
#
# Writing to index.rst directly (rather than a separate include fragment)
# is required because Sphinx's autosummary pre-scan only processes files
# it discovers as source documents.  A ``.. include::`` fragment is
# expanded by docutils at parse time — after the pre-scan — so stubs
# would never be generated from an included file.
#
# Developers never need to touch index.rst when new public submodules
# are added to pamola_core/__init__.py: the next sphinx-build picks
# them up automatically.
# ---------------------------------------------------------------------------

_INDEX_RST_TEMPLATE = """\
PAMOLA Core API Reference
=========================

**PAMOLA Core** is an open-source Python library for privacy-preserving data
operations developed by `Realm Inveo Inc. <https://realminveo.com>`_ and
`DGT Network Inc. <https://dgt.world>`_.

Complete reference for every public symbol exported by ``pamola_core``.
All public APIs are importable directly from the top-level package.

Quick Example
-------------

.. code-block:: python

   import pamola_core as pc

   df = pc.read_csv("data.csv")
   op = pc.FullMaskingOperation(...)
   result = op.execute(df)


API Reference
-------------

.. autosummary::
   :toctree: generated

{entries}

----

Indices and Tables
------------------

* :ref:`genindex`
* :ref:`modindex`
* :ref:`search`
"""


def _discover_public_submodules() -> list:
    """Return ordered list of top-level public submodule dotted names.

    Reads pamola_core/__init__.py via ast and collects every unique
    ``pamola_core.X`` name found in ImportFrom nodes, preserving the
    order in which they first appear.
    """
    init_path = Path(__file__).parent.parent / "pamola_core" / "__init__.py"
    source = init_path.read_text(encoding="utf-8")
    tree = ast.parse(source)

    seen: set = set()
    modules: list = []
    for node in ast.walk(tree):
        if isinstance(node, ast.ImportFrom):
            if node.module and node.module.startswith("pamola_core."):
                # Keep only the first two parts: pamola_core.X
                top_sub = ".".join(node.module.split(".")[:2])
                # Skip private submodules (e.g. pamola_core._version)
                if top_sub.split(".")[-1].startswith("_"):
                    continue
                if top_sub not in seen:
                    seen.add(top_sub)
                    modules.append(top_sub)
    return modules


def _write_index_rst() -> None:
    """Write docs/index.rst from the discovered public submodules.

    The file contains a complete ``.. autosummary::`` directive that lists
    ``pamola_core`` followed by every public submodule.  No ``:recursive:``
    flag is used: deep sub-packages are documented within each module's own
    stub page (via the autosummary template), not promoted into the
    top-level sidebar.
    """
    modules = _discover_public_submodules()
    entry_lines = ["   pamola_core"] + [f"   {m}" for m in modules]
    content = _INDEX_RST_TEMPLATE.format(entries="\n".join(entry_lines))

    index_path = Path(__file__).parent / "index.rst"
    index_path.write_text(content, encoding="utf-8")


_write_index_rst()


