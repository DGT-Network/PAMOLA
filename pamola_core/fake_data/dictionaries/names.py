def get_names(language: str = "ru",
              gender: Optional[str] = None,
              name_type: str = "first_name") -> List[str]:
    """
    Get names based on language, gender, and name type.

    Args:
        language: Language code ("ru", "en", "vn")
        gender: Gender code ("M", "F" or None)
        name_type: Type of name ("first_name", "last_name", "middle_name", "full_name")

    Returns:
        List[str]: List of names matching the criteria
    """
    language = language.lower()
    gender_normalized = gender.upper() if gender else None

    # Russian names
    if language == "ru":
        if name_type == "first_name":
            if gender_normalized == "M":
                return get_ru_male_first_names()
            elif gender_normalized == "F":
                return get_ru_female_first_names()
            else:
                return get_ru_male_first_names() + get_ru_female_first_names()
        elif name_type == "last_name":
            if gender_normalized == "M":
                return get_ru_male_last_names()
            elif gender_normalized == "F":
                return get_ru_female_last_names()
            else:
                return get_ru_male_last_names() + get_ru_female_last_names()
        elif name_type == "middle_name":
            if gender_normalized == "M":
                return get_ru_male_middle_names()
            elif gender_normalized == "F":
                return get_ru_female_middle_names()
            else:
                return get_ru_male_middle_names() + get_ru_female_middle_names()

    # English names
    elif language in ["en", "us", "gb"]:
        if name_type == "first_name":
            if gender_normalized == "M":
                return get_en_male_first_names()
            elif gender_normalized == "F":
                return get_en_female_first_names()
            else:
                return get_en_male_first_names() + get_en_female_first_names()
        elif name_type == "last_name":
            return get_en_last_names()

    # Vietnamese names
    elif language == "vn":
        if name_type == "full_name":
            if gender_normalized == "M":
                return get_vn_male_names()
            elif gender_normalized == "F":
                return get_vn_female_names()
            else:
                return get_vn_male_names() + get_vn_female_names()

    # Return empty list if no matching dictionary found
    return []


